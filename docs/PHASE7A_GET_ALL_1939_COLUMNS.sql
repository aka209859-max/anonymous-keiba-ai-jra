-- ============================================================
-- 全カラムリスト（テーブル名・カラム名・データ型・順序）
-- ============================================================
SELECT 
    table_name,
    ordinal_position,
    column_name,
    data_type,
    character_maximum_length
FROM information_schema.columns
WHERE table_schema = 'public'
  AND (table_name LIKE 'jvd_%' OR table_name LIKE 'jrd_%')
ORDER BY 
    table_name,
    ordinal_position;

